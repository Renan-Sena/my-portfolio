let currentLang = 'pt';

function applyLang(lang) {
    currentLang = lang;
    document.documentElement.setAttribute('lang', lang === 'pt' ? 'pt-BR' : 'en');

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        if (i18n[lang] && i18n[lang][key] !== undefined) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA' || el.tagName === 'SELECT') {
                if (el.type !== 'button' && el.type !== 'submit') {
                    el.placeholder = i18n[lang][key];
                } else {
                    el.innerHTML = i18n[lang][key];
                }
            } else {
                el.innerHTML = i18n[lang][key];
            }
        }
    });

    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
        const key = el.dataset.i18nPh;
        if (i18n[lang] && i18n[lang][key] !== undefined) {
            el.placeholder = i18n[lang][key];
        }
    });

    document.querySelectorAll('#countryCode option[data-i18n]').forEach(opt => {
        const key = opt.dataset.i18n;
        if (i18n[lang] && i18n[lang][key]) {
            opt.textContent = i18n[lang][key];
        }
    });

    const btn = document.getElementById('langToggle');
    if (btn) {
        btn.textContent = lang === 'pt' ? 'EN' : 'PT';
        btn.setAttribute('aria-label', lang === 'pt' ? 'Switch to English' : 'Trocar para Português');
    }

    updateSEO(lang);
    window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
}

function updateSEO(lang) {
    document.title = i18n[lang]["seo.title"];
    let metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc) metaDesc.setAttribute("content", i18n[lang]["seo.desc"]);
    let ogTitle = document.querySelector('meta[property="og:title"]');
    let ogDesc = document.querySelector('meta[property="og:description"]');
    let ogLocale = document.querySelector('meta[property="og:locale"]');
    if (ogTitle) ogTitle.setAttribute("content", i18n[lang]["seo.title"]);
    if (ogDesc) ogDesc.setAttribute("content", i18n[lang]["seo.desc"]);
    if (ogLocale) ogLocale.setAttribute("content", i18n[lang]["og.locale"]);
    let twTitle = document.querySelector('meta[name="twitter:title"]');
    let twDesc = document.querySelector('meta[name="twitter:description"]');
    if (twTitle) twTitle.setAttribute("content", i18n[lang]["twitter.title"]);
    if (twDesc) twDesc.setAttribute("content", i18n[lang]["twitter.desc"]);
}